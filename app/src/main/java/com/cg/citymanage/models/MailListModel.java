public class MailListModel {

    private String index;
    private String id;
    private String tel;
    private String name;

    public MailListModel() {
    }

    public MailListModel(String id,String tel, String name) {
        this.index = FirstLetterUtil.getFirstLetter(name);
        this.id = id;
        this.tel = tel;
        this.name = name;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIndex() {
        return index;
    }

    public void setIndex(String index) {
        this.index = index;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
